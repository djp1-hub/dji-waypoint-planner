export function getTranslation(language: Language, key: string): string {
  const current = translations[language]?.[key];

  if (typeof current === 'string') {
    return current;
  }

  const fallback = translations.en?.[key];

  if (typeof fallback === 'string') {
    return fallback;
  }

  return key;
}

